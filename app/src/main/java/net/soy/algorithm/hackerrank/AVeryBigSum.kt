package net.soy.algorithm.hackerrank

/**
 * Algorithm
 * A Very Big Sum
 * @author leesoyoung
 * @since 2022/04/17
 */

class AVeryBigSum {
    /**
     * 모든 배열 요소의 합계를 반환해야 합니다.
     * aVeryBigSum에는 다음과 같은 매개 변수가 있습니다. int ar[n]: 정수의 배열.
     */
    fun solution(ar: Array<Long>): Long {
        return ar.sum()
    }
}